export function localizedResultExplanation(
  controlId: string,
  status: "PASS" | "FAIL" | "MISSING" | "WARNING",
  fallback: string,
  locale: Locale,
  threshold: string,
): string {
  if (locale === "en") return fallback;
  if (controlId === "CTRL-APPROVAL") {
    return status === "PASS"
      ? `La transaction de 12 480 EUR ne dépasse pas le seuil d’approbation de ${Number(threshold).toLocaleString("fr-FR")} EUR.`
      : `La transaction de 12 480 EUR dépasse le seuil, mais un seul approbateur distinct est enregistré ; deux sont requis.`;
  }
  return frenchResultCopy[controlId]?.[status] ?? fallback;
}

export function localizedMissingEvidence(controlId: string, locale: Locale, description: string, source: string) {
  if (locale === "en") return { description, source };
  if (controlId === "CTRL-DELIVERY") return { description: "Une preuve de livraison est requise.", source: "Bon de livraison" };
  if (controlId === "CTRL-BANK") return { description: "Une vérification indépendante des nouvelles coordonnées bancaires est requise.", source: "Preuve de vérification bancaire ou journal de rappel" };
  return { description, source };
}
